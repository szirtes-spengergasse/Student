public class StudentInDerSpengergasse
{ 
    private String  name;
    private int     alter;
    private boolean matura;
    
    public String getName()
    {
        return name;
    
    }
    
    public int getAlter()
    {
        return alter;
    }
    
    public boolean getMatura()
    {
        return matura;
    }
    
    public void setName(String setName)
    {
        name = setName;
    }
    
    public void setAlter(int setAlter)
    {
        alter = setAlter;
    }
    
    public void setMatura(boolean setMatura)
    {
        matura = setMatura;
    }
}    